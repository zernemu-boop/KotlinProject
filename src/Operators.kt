fun main() {

    //Arithmetic operators
    var num1 = 50
    var num2 = 30

    println(num1 + num2)
    println(num1 - num2)
    println(num1 * num2)
    println(num1 / num2)
    println(num1 % num2)

    println()

    //Comparison Operators

    println(num1 < num2)
    println(num1 > num2)
    println(num1 >= num2)
    println(num1 >= num2)
    println(num1 == num2)
    println(num1 != num2)

    println()

    //Assignment operators - assign values in variables
    var x = 67
    println(x)

    x += 2
    println(x)

    x %= 2
    println(x)

    println()

    //logical operators -&&-and , ||-or, !-not
    println(100 == 80 && 30 > 5)
    println(100 == 80 && 30 > 5)
}